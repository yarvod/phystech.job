<template>
  <div>
    <b-card
      tag="article"
      class="mb-2"
      :title=resume.title
    >
      <template #header>
        <b>Резюме</b>
      </template>
      <b-card-text>
        <b>Категория: </b>
        {{resume.category}}
        <br>
        <b-row v-if="resume.tags">
          <b-col>
             <b>Тэги:</b>
            <b-link class="m-1" v-for="tag in resume.tags" :key="tag">
              {{ tag }}
            </b-link>
          </b-col>
        </b-row>
      </b-card-text>

      <hr>

      <b-button variant="outline-primary" class="m-1"
        v-if="edit"
        @click="$router.push({name: 'resume_edit', params: {resumeId: resume.id}})"
      >
        Редактировать
      </b-button>
      <div v-else>
        <b-button variant="outline-primary" class="m-1"
                @click="$router.push({name: 'resume_details', params: {resumeId: resume.id}})">
          Подробнее
        </b-button>

        <b-checkbox
          v-if="this.$store.getters.user && !this.$store.getters.user.employee"
          v-model="liked"
          @change="onlike"
        >
          like
        </b-checkbox>
      </div>
      
      <template #footer>
        <small class="text-muted">
          <div v-if="resume.is_published">
            <b>Опубликовано: </b>
            {{resume.published|formatDate}}
          </div>
          <div v-else>
            <b>Не опубликовано</b>
          </div>
        </small>
      </template>

    </b-card>
  </div>
</template>

<script>

export default {
  name: "resumeItem",
  props: ['resume', 'edit'],
  data () {
    return {
      liked: Boolean
    }
  },
  methods: {
    onlike () {
      this.$store.dispatch('setResumeLike', {id: this.$store.getters.user.employer.id, f_r_id: this.resume.id})
    },
    setlike () {
      let f_r = this.$store.getters.user.favorites.resumes;
      this.liked = f_r.includes(this.resume.id)
    }
  },
  mounted () {
    if (this.$store.getters.user && !this.edit && !this.$store.getters.user.employee) {
      this.setlike()
    }
  }





}
</script>

