<template>
	<div>
		<ul>
			<ToDoItem
				v-for="(todo, i) of todos"
				v-bind:todo="todo"
				v-bind:index="i"
				:key="todo.id"
				v-on:remove-todo="removeTodo"
			/>
		</ul>
	</div>
</template>


<script>
import ToDoItem from '@/components/ToDoItem'
export default {
		props: ['todos'],
    components: {
        ToDoItem: ToDoItem
    },
		methods: {
			removeTodo(id) {
				this.$emit('remove-todo', id)
			}
		}
}
</script>


